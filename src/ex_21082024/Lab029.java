package ex_21082024;

public class Lab029 {
    public static void main(String[] args) {
        // Compound Assignment operator
        int a = 10;
        int b = 20;
        // +=, -=, *=, /=, %=

        a+=10; // a= a+10
        b-=10; // b = b-10
        System.out.println(a);
        System.out.println(b);
    }
}
